// Operadores aritméticos
/*
+
-
*
/
%
**
*/

const numero1 = "2";
const numero2 = 2;

console.log(numero1 + numero2); // -> 22
console.log(numero1 == numero2); // -> true
console.log(numero1 === numero2); // -> false
console.log(numero1 != numero2); // -> false
console.log(numero1 !== numero2); // -> true
console.log(numero2 % numero1 === 0); // -> true
